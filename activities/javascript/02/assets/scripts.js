document.getElementById('english-grade').addEventListener('keyup', function(){

    const englishGrade = parseInt(document.getElementById('english-grade').value);
    const mathGrade = parseInt(document.getElementById('math-grade').value);
    const dataStrucGrade = parseInt(document.getElementById('data-struc-grade').value);
    const compProgGrade = parseInt(document.getElementById('comp-prog-grade').value);
    const webProgGrade = parseInt(document.getElementById('web-prog-grade').value);
    
    const totalGrade = englishGrade + mathGrade + dataStrucGrade + compProgGrade + webProgGrade;

    const averageGrade = totalGrade / 5;

    document.getElementById('total-grade').value = totalGrade;
    document.getElementById('average-grade').value = averageGrade;

});

